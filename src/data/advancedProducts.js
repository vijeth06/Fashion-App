export const outfitCompatibilityEngine = {
  calculateStyleVector: (items) => {

    return items.reduce((acc, item) => {
      return acc.map((val, idx) => val + item.aiMetadata.styleVector[idx]);
    }, [0, 0, 0, 0, 0]).map(sum => sum / items.length);
  },

  generateOutfitScore: (outfit) => {
    const baseCompatibility = outfit.reduce((score, item, idx) => {
      const others = outfit.filter((_, i) => i !== idx);
      return others.reduce((itemScore, other) => {
        const vectorDistance = item.aiMetadata.styleVector.reduce((dist, val, i) => 
          dist + Math.abs(val - other.aiMetadata.styleVector[i]), 0
        );
        return itemScore + (1 - vectorDistance / 5); // Normalize to 0-1
      }, score);
    }, 0);
    
    return Math.min(baseCompatibility / (outfit.length * (outfit.length - 1)), 1);
  },

  predictTrendViability: (items) => {
    return items.reduce((sum, item) => 
      sum + item.aiMetadata.trendPrediction.futureViability, 0
    ) / items.length;
  }
};

